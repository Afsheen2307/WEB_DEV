console.log("Odd number");
for(let i=1;i<=15;i=i+2){
    console.log(i);
}
console.log("Backward");
for(let i=15;i>=1;i=i-2){
    console.log(i);
}

console.log("Even Numbers");
for(let i=2;i<=20;i=i+2){
    console.log(i);
}
console.log("Backward");
for(let i=10;i>=2;i=i-2){
    console.log(i);
}